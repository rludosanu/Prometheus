export default function reducer(state = workouts, action) {
  const { type, payload } = action;

  switch (type) {
    case 'ADD_WORKOUT':
      return {
        ...state,
        error: null,
        isLoading: true
      };
    case 'UPDATE_WORKOUT':
      return {
        token: payload.token,
        error: null,
        isLoading: false
      };
    case 'DELETE_WORKOUT':
      return {
        token: null,
        error: payload.error || null,
        isLoading: false
      };
    default:
      return state;
  }
}
